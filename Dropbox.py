from turtle import *
def diamond(x, y):
    seth(0)
    setpos(x, y)
    stamp()
    seth(180)
    forward(53)
    stamp()
speed(1)
bgcolor("white")
pen(pencolor="#0060FF")
shape("triangle")
fillcolor('#0060FF')
penup()
width(1)
shapesize(5)
diamond(20, 6)
diamond(100, 70)
diamond(-60, 70)
diamond(100, 172)
diamond(-60, 172)
done()
input()